#include<stdio.h>
// Ternary


int main() {
    int age;
    printf("Enter Your Age: ");
    scanf("%d", &age);

    // This is Ternary Statment
    // This only works when the result can be only True or False
    // Only works for one statement

    age >= 18 ? printf("Adult\n") : printf("Not Adult\n");





    return 0;

}


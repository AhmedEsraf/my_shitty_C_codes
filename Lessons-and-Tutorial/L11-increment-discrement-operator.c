#include<stdio.h>

int main() {
    // increment operator 
    // i++ (post increment ), ++i (pre increment)
    int i=1;
    
    printf("%d\n",i++); // use then increase so it outputs 1
    printf("%d\n ",i);

    printf("%d\n",++i); // increase then use

    // decrement operator
    // --i , i++

    printf("%d\n",i--); // use then decrease
    printf("%d\n ",i);

    printf("%d\n",--i); // decrease then use



    return 0;

}